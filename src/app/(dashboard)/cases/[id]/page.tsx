function InvestigationTimeline({
  investigation,
}: {
  investigation: InvestigationDetail;
}) {
  const artifacts = investigation.artifacts;
  const findings = investigation.findings;
  const metrics = investigation.metrics;

  const formatTime = (d?: string | null) =>
    d ? new Date(d).toLocaleTimeString("en-US", { hour12: false, hour: "2-digit", minute: "2-digit", second: "2-digit" }) : "Unknown";

  const steps = [
    {
      time: formatTime(investigation.created_at),
      label: "IOC submitted",
      detail: `Target: ${investigation.target} (${investigation.target_type})`,
      status: "completed",
    },
    ...artifacts.map((art) => {
      const artFindings = findings.filter((f) =>
        f.evidence?.some((ev: Evidence) => (ev as Evidence & { artifact_id?: string }).artifact_id === art.id)
      );
      const artEvidence = (investigation.all_evidence || []).filter(
        (ev: Evidence) => ev.artifact_id === art.id
      );
      return {
        time: formatTime(art.fetched_at || ((art as unknown) as Record<string, unknown>).created_at as string),
        label: `${art.source} artifact ${art.is_reused ? "(cached)" : "fetched"}`,
        detail: `${artEvidence.length} evidence fact${artEvidence.length !== 1 ? "s" : ""} extracted${artFindings.length > 0 ? `, ${artFindings.length} finding${artFindings.length !== 1 ? "s" : ""}` : ""}`,
        status: "completed",
      };
    }),
    ...(investigation.status === "COMPLETED" || investigation.status === "FAILED"
      ? [
          {
            time: investigation.completed_at
              ? formatTime(investigation.completed_at)
              : "",
            label: investigation.status === "COMPLETED"
              ? `Score computed under Scoring Profile v${investigation.scoring_profile_version ?? "1.0"}`
              : "Investigation failed",
            detail: investigation.status === "COMPLETED"
              ? `Final verdict: ${investigation.final_score ?? 0}/100 — ${findings.length} finding${findings.length !== 1 ? "s" : ""}`
              : `All sources failed: ${investigation.failed_sources.map((f) => f.source).join(", ")}`,
            status: investigation.status === "COMPLETED" ? "completed" : "failed",
          },
        ]
      : []),
  ];

  return (
    <div className="panel" style={{ padding: "1.25rem" }}>
      <div
        style={{
          fontFamily: "var(--font-display)",
          fontSize: "1rem",
          fontWeight: 500,
          marginBottom: "1.25rem",
          color: "var(--text-primary)",
        }}
      >
        Investigation Timeline
      </div>

      <div className="timeline-spine">
        {steps.map((step, idx) => (
          <div
            key={idx}
            className={`timeline-step ${step.status}`}
            style={{ marginBottom: "1rem" }}
          >
            <div style={{ display: "flex", alignItems: "flex-start", gap: "8px" }}>
              <span className="mono text-tertiary" style={{ fontSize: "0.6875rem", flexShrink: 0, marginTop: "2px", minWidth: "56px" }}>
                {step.time}
              </span>
              <div>
                <div style={{ fontSize: "0.875rem", color: "var(--text-primary)", fontWeight: 500 }}>
                  {step.label}
                </div>
                <div className="mono" style={{ fontSize: "0.75rem", color: "var(--text-tertiary)", marginTop: "2px" }}>
                  {step.detail}
                </div>
              </div>
            </div>
          </div>
        ))}
      </div>

      {metrics && (
        <div
          style={{
            marginTop: "1.25rem",
            paddingTop: "1rem",
            borderTop: "var(--border)",
          }}
        >
          <div
            style={{
              fontFamily: "var(--font-mono)",
              fontSize: "0.625rem",
              textTransform: "uppercase",
              letterSpacing: "0.1em",
              color: "var(--text-tertiary)",
              marginBottom: "0.75rem",
            }}
          >
            Investigation Metrics
          </div>
          <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: "8px" }}>
            {[
              { label: "Artifacts", value: metrics.artifacts_fetched },
              { label: "Evidence", value: metrics.evidence_extracted },
              { label: "Findings", value: metrics.findings_generated },
              { label: "Failed Sources", value: metrics.failed_sources_count },
              { label: "Execution Time", value: `${(metrics.execution_time_ms / 1000).toFixed(1)}s` },
            ].map(({ label, value }) => (
              <div key={label} style={{ display: "flex", flexDirection: "column", gap: "2px" }}>
                <span className="mono" style={{ fontSize: "0.75rem", color: "var(--text-tertiary)" }}>
                  {label}
                </span>
                <span className="mono" style={{ fontSize: "0.875rem", color: "var(--text-primary)" }}>
                  {value}
                </span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}

export default function InvestigationDetailPage() {
  const params = useParams();
  const id = params.id as string;
  const [data, setData] = useState<InvestigationDetail | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [newNote, setNewNote] = useState("");
  const [newTag, setNewTag] = useState("");
  const [savingNote, setSavingNote] = useState(false);

  const fetchData = useCallback(async () => {
    const res = await fetch(`/api/investigations/${id}`);
    if (!res.ok) {
      setError("Investigation not found");
      return;
    }
    const d = await res.json() as InvestigationDetail;
    setData(d);
  }, [id]);

  useEffect(() => {
    // eslint-disable-next-line react-hooks/set-state-in-effect
    fetchData();
  }, [fetchData]);

  // Poll while in-progress
  useEffect(() => {
    if (!data) return;
    const inProgress = !["COMPLETED", "FAILED"].includes(data.status);
    if (!inProgress) return;

    let active = true;
    const interval = setInterval(async () => {
      try {
        const res = await fetch(`/api/investigations/${id}`);
        if (!res.ok) return;
        const d = await res.json() as InvestigationDetail;
        if (active) {
          setData(d);
        }
      } catch (err) {
        console.error("Polling error:", err);
      }
    }, 2000);

    return () => {
      active = false;
      clearInterval(interval);
    };
  }, [data?.status, id]);

  const handleAddNote = async () => {
    if (!newNote.trim()) return;
    setSavingNote(true);
    await fetch(`/api/investigations/${id}/notes`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ content: newNote }),
    });
    setNewNote("");
    setSavingNote(false);
    fetchData();
  };

  const handleAddTag = async () => {
    if (!newTag.trim()) return;
    await fetch(`/api/investigations/${id}/tags`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ tagName: newTag }),
    });
    setNewTag("");
    fetchData();
  };

  const handleRemoveTag = async (tagId: string) => {
    await fetch(`/api/investigations/${id}/tags`, {
      method: "DELETE",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ tagId }),
    });
    fetchData();
  };

  const handleUpdateDisposition = async (findingId: string, status: string, note?: string) => {
    const res = await fetch(`/api/investigations/${id}/findings/${findingId}`, {
      method: "PATCH",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ status, note }),
    });
    if (res.ok) {
      fetchData();
    }
    return res;
  };

  if (error) return (
    <div style={{ padding: "2rem 2.5rem", color: "var(--accent-severe)" }}>{error}</div>
  );

  if (!data) {
    return (
      <div style={{ padding: "2rem 2.5rem" }}>
        <div className="redacted-line" style={{ width: "300px", height: "24px", marginBottom: "1rem" }} />
        <div className="redacted-block" style={{ width: "100%", height: "200px" }} />
      </div>
    );
  }

  const inProgress = !["COMPLETED", "FAILED"].includes(data.status);
  const uniqueSources = [...new Set(data.artifacts.map((a) => a.source))];

  return (
    <>
      <div className="detail-page">
        <div className="detail-header">
          <div className="detail-case-number">
            <span className="case-number-prominent">{data.case_number}</span>
            {" — "}
            <span
              className={`status-pill ${
                inProgress ? "status-in-progress" : data.status === "COMPLETED" ? "status-completed" : "status-failed"
              }`}
            >
              {STATUS_LABELS[data.status]}
            </span>
          </div>
          <div className="detail-target">{data.target}</div>
          <div className="detail-meta">
            <span className="mono" style={{ fontSize: "0.75rem", color: "var(--text-tertiary)" }}>
              {data.target_type}
            </span>
            <span style={{ fontSize: "0.75rem", color: "var(--text-tertiary)" }}>
              Opened {new Date(data.created_at).toLocaleString()}
            </span>
            {data.tags.map((tag) => (
              <span key={(tag as { id: string }).id} className="tag-pill">
                {(tag as { name: string }).name}
                <button
                  className="tag-remove"
                  onClick={() => handleRemoveTag((tag as { id: string }).id)}
                  aria-label={`Remove tag ${(tag as { name: string }).name}`}
                >
                  ×
                </button>
              </span>
            ))}
            <form
              onSubmit={(e) => { e.preventDefault(); handleAddTag(); }}
              style={{ display: "flex", gap: "4px" }}
            >
              <input
                className="input"
                value={newTag}
                onChange={(e) => setNewTag(e.target.value)}
                placeholder="Add tag…"
                style={{ width: "120px", padding: "3px 8px", fontSize: "0.75rem" }}
                id="add-tag-input"
              />
            </form>
            {data.status === "COMPLETED" && (
              <a
                href={`/api/investigations/${id}/pdf`}
                target="_blank"
                rel="noreferrer"
                className="btn btn-secondary"
                style={{ fontSize: "0.75rem", padding: "4px 8px", marginLeft: "auto" }}
              >
                Download Forensic Briefing (PDF)
              </a>
            )}
          </div>
        </div>

        {/* IOC History banner */}
        {data.prior_investigations && data.prior_investigations.length > 0 && (
          <div className="prior-inv-banner">
            Previously seen {data.prior_investigations.length} time{data.prior_investigations.length !== 1 ? "s" : ""}.{" "}
            Last investigation: {data.prior_investigations[0].case_number} —
            Score: {data.prior_investigations[0].final_score ?? "—"} —{" "}
            {new Date(data.prior_investigations[0].created_at).toLocaleDateString()}
          </div>
        )}

        {/* Source status bar */}
        {uniqueSources.length > 0 && (
          <SourceStatusBar
            failedSources={data.failed_sources}
            sources={uniqueSources}
          />
        )}



        {/* In-progress redaction */}
        {inProgress && (
          <div style={{ marginBottom: "1.5rem" }}>
            <div className="redacted-block" style={{ height: "120px", borderRadius: "var(--radius-sm)" }} />
            <div style={{ marginTop: "8px", fontSize: "0.8125rem", color: "var(--accent-open)", fontFamily: "var(--font-mono)" }}>
              {STATUS_LABELS[data.status]}
            </div>
          </div>
        )}

        <div className="detail-layout">
          <div className="detail-main">
            {/* Findings */}
            {data.findings.length > 0 && (
              <div style={{ marginBottom: "1.5rem" }}>
                <div className="section-title">Findings ({data.findings.length})</div>
                <div className="findings-list">
                  {data.findings.map((f) => (
                    <FindingCard 
                      key={f.id} 
                      finding={f as any} 
                      artifacts={data.artifacts} 
                      isCompleted={data.status === "COMPLETED"}
                      onUpdateDisposition={(status, note) => handleUpdateDisposition(f.id, status, note)}
                    />
                  ))}
                </div>
              </div>
            )}

            {/* Score breakdown removed from here, now in sidebar */}

            {/* Timeline */}
            <div style={{ marginBottom: "1.5rem" }}>
              <InvestigationTimeline investigation={data} />
            </div>

            {/* Analyst Notes */}
            <div className="notes-section">
              <div className="section-title">Analyst Notes</div>
              <div style={{ display: "flex", gap: "8px", marginBottom: "1rem" }}>
                <textarea
                  className="input"
                  value={newNote}
                  onChange={(e) => setNewNote(e.target.value)}
                  placeholder="Add an analyst note…"
                  rows={3}
                  id="analyst-note-input"
                  style={{ flex: 1 }}
                />
                <button
                  className="btn btn-secondary"
                  onClick={handleAddNote}
                  disabled={savingNote || !newNote.trim()}
                  id="save-note-btn"
                  style={{ alignSelf: "flex-start" }}
                >
                  Save
                </button>
              </div>
              {data.notes.map((note) => (
                <div key={note.id} className="note-item">
                  <div className="note-content">{note.content}</div>
                  <div className="note-time">
                    {new Date(note.created_at).toLocaleString()}
                  </div>
                </div>
              ))}
            </div>
          </div>

          {/* Sticky sidebar */}
          <div className="detail-sidebar" style={{ position: "sticky", top: "1.5rem" }}>
            <EvidenceLedger investigation={data} />
          </div>
        </div>
      </div>
    </>
  );
}
