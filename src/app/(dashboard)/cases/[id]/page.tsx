"use client";

import { useEffect, useState, useCallback } from "react";
import { useParams } from "next/navigation";
import { confidenceLabel } from "@/lib/pipeline/confidence";
import { MITRE_MAPPINGS } from "@/lib/attack/mitreMapping";
import type { InvestigationDetail, Finding, Evidence, Artifact } from "@/lib/pipeline/types";
import RadarChart from "@/components/RadarChart";

const ANALYZER_BADGES: Record<string, string> = {
  VirusTotalAnalyzer: "VT",
  AbuseIPDBAnalyzer: "ABUSE",
  DomainAgeAnalyzer: "WHOIS",
  EntropyAnalyzer: "ENT",
  ASNReputationAnalyzer: "ASN",
  HomographAnalyzer: "HOM",
  EmailAuthAnalyzer: "HDR",
  FingerprintAnalyzer: "FP",
  CVEPriorityAnalyzer: "CVE",
};

const STATUS_LABELS: Record<string, string> = {
  CREATED: "Created",
  FETCHING_ARTIFACTS: "Fetching sources…",
  EXTRACTING_EVIDENCE: "Extracting evidence…",
  RUNNING_ANALYZERS: "Running analyzers…",
  SCORING: "Computing score…",
  COMPLETED: "Completed",
  FAILED: "Failed",
};

function ScoreColor(score: number): string {
  if (score >= 75) return "var(--severity-critical)";
  if (score >= 50) return "var(--severity-high)";
  if (score >= 25) return "var(--severity-medium)";
  return "var(--severity-low)";
}

function StampBadge({ status, onClick, interactive }: { status: string; onClick?: (e: React.MouseEvent) => void; interactive?: boolean }) {
  const [animated, setAnimated] = useState(false);

  useEffect(() => {
    const t = setTimeout(() => setAnimated(true), 100);
    return () => clearTimeout(t);
  }, []);

  const cls = `finding-status status-${status} ${animated ? "stamp-animate" : ""} ${interactive ? "interactive-badge" : ""}`;
  return (
    <span className={cls} onClick={onClick} style={interactive ? { cursor: "pointer" } : {}}>
      {status.replace("_", " ")}
      {interactive && <span style={{ marginLeft: "4px", fontSize: "0.6rem" }}>▼</span>}
    </span>
  );
}

function EvidencePanelContent({
  finding,
  artifacts,
}: {
  finding: Finding & { evidence?: Evidence[] };
  artifacts: Artifact[];
}) {
  const [showRaw, setShowRaw] = useState(false);

  const evidenceList = finding.evidence ?? [];

  // Find the artifact for the first evidence item
  const firstEvidence = evidenceList[0];
  const relatedArtifact = firstEvidence
    ? artifacts.find((a) =>
        evidenceList.some(
          (ev: Evidence) => (ev as Evidence & { artifact_id: string }).artifact_id === a.id
        )
      )
    : null;

  return (
    <div className="evidence-panel-body">
      <div className="ep-meta-row">
        <span className="text-secondary" style={{ fontSize: "0.8125rem" }}>
          Generated by{" "}
          <span className="mono" style={{ color: "var(--accent-open)" }}>
            {finding.generated_by} v{finding.analyzer_version}
          </span>
        </span>
        <span className="text-tertiary" style={{ fontSize: "0.75rem" }}>
          Score contribution:{" "}
          <span className="mono" style={{ color: ScoreColor(finding.confidence_score) }}>
            +{finding.score_contribution}
          </span>
        </span>
      </div>

      {finding.reasoning && (
        <p className="ep-reasoning">{finding.reasoning}</p>
      )}

      {evidenceList.length > 0 && (
        <div className="ep-evidence-list">
          <div className="ep-section-label">Evidence</div>
          {evidenceList.map((ev: Evidence) => (
            <div key={ev.id} className="ep-evidence-row">
              <span className="mono text-tertiary" style={{ fontSize: "0.75rem" }}>
                {ev.fact_type}
              </span>
              <span className="mono" style={{ fontSize: "0.8125rem", color: "var(--text-primary)" }}>
                {typeof ev.fact_value === "object"
                  ? JSON.stringify(ev.fact_value)
                  : String(ev.fact_value)}
              </span>
            </div>
          ))}
        </div>
      )}

      {relatedArtifact && (
        <div style={{ marginTop: "1rem" }}>
          <button
            className="btn btn-secondary"
            style={{ fontSize: "0.8125rem", padding: "6px 12px" }}
            onClick={() => setShowRaw(!showRaw)}
            id={`raw-artifact-toggle-${finding.id}`}
          >
            {showRaw ? "Hide Raw Artifact" : "View Raw Artifact →"}
          </button>
          {showRaw && (
            <pre className="raw-artifact-view">
              {JSON.stringify(relatedArtifact.raw_response, null, 2)}
            </pre>
          )}
        </div>
      )}
      {(finding as any).disposition_note && (
        <div style={{ marginTop: "12px", padding: "8px", background: "var(--bg-surface)", borderLeft: "2px solid var(--accent-open)", fontSize: "0.8125rem", borderRadius: "0 4px 4px 0" }}>
          <div style={{ color: "var(--text-tertiary)", fontSize: "0.6875rem", textTransform: "uppercase", marginBottom: "4px", fontFamily: "var(--font-mono)" }}>Analyst Disposition Note</div>
          <div style={{ color: "var(--text-primary)" }}>{(finding as any).disposition_note}</div>
        </div>
      )}
    </div>
  );
}

function FindingCard({
  finding,
  artifacts,
  isCompleted,
  onUpdateDisposition,
}: {
  finding: Finding & { evidence?: Evidence[], disposition_note?: string | null };
  artifacts: Artifact[];
  isCompleted?: boolean;
  onUpdateDisposition?: (status: string, note?: string) => Promise<any>;
}) {
  const [open, setOpen] = useState(false);
  const [showDispositionForm, setShowDispositionForm] = useState(false);
  const [dispositionStatus, setDispositionStatus] = useState("CONFIRMED");
  const [dispositionNote, setDispositionNote] = useState("");
  const [savingDisposition, setSavingDisposition] = useState(false);

  const badge = ANALYZER_BADGES[finding.generated_by] ?? finding.generated_by.slice(0, 4).toUpperCase();
  const conf = confidenceLabel(finding.confidence_score);

  const handleDispositionClick = (e: React.MouseEvent) => {
    e.stopPropagation();
    if (isCompleted && onUpdateDisposition) {
      setShowDispositionForm(!showDispositionForm);
    }
  };

  const submitDisposition = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!onUpdateDisposition) return;
    setSavingDisposition(true);
    await onUpdateDisposition(dispositionStatus, dispositionNote);
    setSavingDisposition(false);
    setShowDispositionForm(false);
    setDispositionNote("");
  };

  return (
    <div className="finding-card evidence-tag">
      <div
        className="finding-card-header"
        onClick={() => setOpen(!open)}
        role="button"
        tabIndex={0}
        onKeyDown={(e) => e.key === "Enter" && setOpen(!open)}
        id={`finding-${finding.id}`}
        aria-expanded={open}
      >
        <div className="finding-card-left">
          <span
            className="analyzer-badge"
            title={finding.generated_by}
            aria-label={finding.generated_by}
          >
            {badge}
          </span>
          <span className="finding-claim">{finding.claim}</span>
        </div>
        <div className="finding-card-right">
          {finding.attack_techniques?.map((t) => (
            <a
              key={t}
              href={MITRE_MAPPINGS[t]?.url ?? `https://attack.mitre.org/techniques/${t.split('.')[0]}/`}
              target="_blank"
              rel="noreferrer"
              className="severity-badge severity-INFO"
              style={{ textDecoration: "none" }}
              onClick={(e) => e.stopPropagation()}
              title={MITRE_MAPPINGS[t]?.techniqueName}
            >
              {t}
            </a>
          ))}
          <span className={`severity-badge severity-${finding.severity}`}>
            {finding.severity}
          </span>
          <span
            className="mono"
            style={{ fontSize: "0.75rem", color: "var(--text-tertiary)" }}
          >
            {finding.confidence_score}
            <span style={{ marginLeft: "2px", opacity: 0.6 }}>({conf})</span>
          </span>
          <StampBadge 
            status={finding.status} 
            onClick={handleDispositionClick} 
            interactive={isCompleted && !!onUpdateDisposition} 
          />
          <span style={{ color: "var(--text-tertiary)", fontSize: "0.875rem" }}>
            {open ? "▲" : "▼"}
          </span>
        </div>
      </div>

      {showDispositionForm && (
        <div style={{ padding: "12px 14px", background: "var(--bg-surface-2)", borderTop: "var(--border)" }} onClick={(e) => e.stopPropagation()}>
          <div style={{ fontFamily: "var(--font-mono)", fontSize: "0.6875rem", textTransform: "uppercase", marginBottom: "8px", color: "var(--text-secondary)" }}>Update Disposition</div>
          <form onSubmit={submitDisposition}>
            <select 
              value={dispositionStatus}
              onChange={(e) => setDispositionStatus(e.target.value)}
              className="input"
              style={{ padding: "4px 8px", fontSize: "0.75rem", marginBottom: "8px", width: "100%", maxWidth: "200px", display: "block" }}
            >
              <option value="CONFIRMED">Confirmed</option>
              <option value="CLEARED">Cleared</option>
              <option value="UNDER_REVIEW">Under Review</option>
              <option value="FLAGGED">Flagged</option>
            </select>
            {["CONFIRMED", "CLEARED"].includes(dispositionStatus) && (
              <textarea
                value={dispositionNote}
                onChange={(e) => setDispositionNote(e.target.value)}
                className="input"
                placeholder="Reasoning (min 10 chars)..."
                rows={2}
                style={{ fontSize: "0.75rem", marginBottom: "8px", width: "100%" }}
                required
                minLength={10}
              />
            )}
            <div style={{ display: "flex", gap: "8px" }}>
              <button type="submit" className="btn btn-secondary" disabled={savingDisposition || (["CONFIRMED", "CLEARED"].includes(dispositionStatus) && dispositionNote.length < 10)} style={{ fontSize: "0.75rem", padding: "4px 8px" }}>
                {savingDisposition ? "Saving..." : "Save"}
              </button>
              <button type="button" className="btn btn-secondary" onClick={() => setShowDispositionForm(false)} style={{ fontSize: "0.75rem", padding: "4px 8px" }}>
                Cancel
              </button>
            </div>
          </form>
        </div>
      )}

      {open && (
        <EvidencePanelContent finding={finding} artifacts={artifacts} />
      )}
    </div>
  );
}

function SourceStatusBar({
  failedSources = [],
  sources,
}: {
  failedSources?: Array<{ source: string; reason: string }>;
  sources: string[];
}) {
  const failedSet = new Set(failedSources.map((f) => f.source));
  const failedMap = Object.fromEntries(failedSources.map((f) => [f.source, f.reason]));

  return (
    <div className="source-status-bar">
      <span className="source-status-label">Sources</span>
      <div className="source-status-list">
        {sources.map((src) => {
          const failed = failedSet.has(src);
          return (
            <span
              key={src}
              className={`source-status-item ${failed ? "source-fail" : "source-ok"}`}
              title={failed ? failedMap[src] : "Success"}
            >
              {failed ? "⚠" : "✓"} {src}
            </span>
          );
        })}
      </div>
    </div>
  );
}

